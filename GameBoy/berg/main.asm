INCLUDE "hardware.inc"
INCLUDE "text-macros.inc"

SECTION "Header", ROM0[$100]
	jp EntryPoint
	ds $150 - @, 0

EntryPoint:
; Wait for VBlank
WaitVBlank:
	ld a, [rLY]
	cp 144
	jp c, WaitVBlank

	; Turn the LCD off
	ld a, 0
	ld [rLCDC], a

    ; Copy sprites to VRAM ----------------------------------------------------
    ; Copy berg tile data
    ld de, bergTileData
    ld hl, $9000
    ld bc, bergTileDataEnd - bergTileData
    call Memcopy
    ; VRAM from $9000 to $9260

    ; Copy letters tiles
    ld de, letters
    ld hl, $9270
    ld bc, lettersEnd - letters
    call Memcopy

    ; Copy tile map
    ld de, TileMap
    ld hl, $9800
    ld bc, TilemapEnd - TileMap
    call Memcopy

    ; Write text
    ; First line
    ld de, $9941
    ld hl, DisplayText01
    call DrawTextTilesLoop
    ; Second line
    ld de, $9961
    ld hl, DisplayText02
    call DrawTextTilesLoop

	; Turn the LCD on ---------------------------------------------------------
	ld a, LCDCF_ON | LCDCF_BGON
	ld [rLCDC], a

	; During the first (blank) frame, initialize display registers
	ld a, %11100100
	ld [rBGP], a

; Game loop -------------------------------------------------------------------
Done:
    jp Done

; =============================================================================

; @param de: Source
; @param hl: Destination
; @param bc: Length
Memcopy:
    ld a, [de]
    ld [hli], a
    inc de
    dec bc
    ld a, b
    or a, c
    jp nz, Memcopy
    ret

; Draw a text on screen
; @param de: Start tile
; @param hl: Text address
DrawTextTilesLoop:
    ; Check for the end of string character 255
    ld a, [hl]
    cp 255 ; Keep writing the string until find value 255.
    ret z

    ; Apply offset because letters sprite don't start at VRAM $9000 and write
    ; the current character (in hl) to the address on the tilemap (in de)
    ld a, [hl]
    add a, 39  ; Offset line
    ld [de], a

    inc hl
    inc de

    ; move to the next character and next background tile
    jp DrawTextTilesLoop

; =============================================================================

DisplayText01:  db "me ve dois", 255
DisplayText02:  db "derby solto", 255

letters: INCBIN "letters.2bpp"
lettersEnd:

bergTileData: INCBIN "berg.2bpp"
bergTileDataEnd:

TileMap: INCBIN "berg.tilemap"
TilemapEnd:

